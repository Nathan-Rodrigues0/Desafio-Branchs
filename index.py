i = int(input("Digte um número de repetição: "))
num = 2

while True:
    print("Código feito")
    print("Você conseguiu")
    print("Parabéns")
    if num <= i:
        num += 1
        continue
    elif num > i :
        print("Finalizado")
        break
    else:
        print("Inacessível")
        break





